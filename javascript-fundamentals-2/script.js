function describeCountry(country, population, capitalCity) {
  console.log(
    `${country} has ${population} million people and its capital city is ${capitalCity}`
  )
}

describeCountry('Costa Rica', 5.094, 'San Jose')
describeCountry('Argentina', 45.38, 'Buenos Aires')
describeCountry('Austrailia', 25.69, 'Canberra')
